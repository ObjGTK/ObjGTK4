#import "OGTKAnyFilter.h"

@implementation OGTKAnyFilter

- (instancetype)init
{
	GtkAnyFilter* gobjectValue = GTK_ANY_FILTER(gtk_any_filter_new());

	@try {
		self = [super initWithGObject:gobjectValue];
	} @catch (id e) {
		g_object_unref(gobjectValue);
		[self release];
		@throw e;
	}

	g_object_unref(gobjectValue);
	return self;
}

- (GtkAnyFilter*)castedGObject
{
	return GTK_ANY_FILTER([self gObject]);
}


@end